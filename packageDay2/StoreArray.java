package packageDay2;

public class StoreArray {
    public static void main(String[] args) {
        int[] marks = new int[5];  
        marks[0] = 50;  
        marks[1] = 60;  
        marks[2] = 70;  
        marks[3] = 80;  
        marks[4] = 90;  

        System.out.println("Marks at index 2: " + marks[2]);  
    }
}
